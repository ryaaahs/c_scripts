#include <stdio.h>

int main(void)
{   
    // See what values are assigned on a non initialized variable
    int a;
    int b;
    int c;
    float d;
    float e;
    float f;

    printf("A: %d\n", a);
    printf("B: %d\n", b);
    printf("C: %d\n", c);
    printf("D: %f\n", d);
    printf("E: %f\n", e);
    printf("F: %f\n", f);
}